/**
 * Get redirect URI for OAuth
 */
export function getOAuthRedirectUri(): string {
  return `${APP_URL}/oauth/callback`;
}

/**
 * Get OAuth config with redirect URI
 */
export function getOAuthConfigWithRedirect(
  server: RemoteMCPServer
): OAuthConfig | undefined {
  if (!server.requiresOAuth || !server.oauthConfig) {
    return undefined;
  }

  return {
    ...server.oauthConfig,
    redirectUri: getOAuthRedirectUri(),
  };
}

/**
 * Filter servers by category
 */
export function getServersByCategory(category: string): RemoteMCPServer[] {
  return REMOTE_SERVERS.filter((s) => s.category === category);
}

/**
 * Search servers
 */
export function searchServers(query: string): RemoteMCPServer[] {
  const lowerQuery = query.toLowerCase();
  return REMOTE_SERVERS.filter(
    (s) =>
      s.name.toLowerCase().includes(lowerQuery) ||
      s.description.toLowerCase().includes(lowerQuery) ||
      s.tags?.some((tag) => tag.toLowerCase().includes(lowerQuery))
  );
}

/**
 * Get server by ID
 */
export function getServerById(id: string): RemoteMCPServer | undefined {
  return REMOTE_SERVERS.find((s) => s.id === id);
}

